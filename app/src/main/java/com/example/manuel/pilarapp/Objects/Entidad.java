public class Entidad {

    private Integer id;
    private String title;
    private String telefonoEntidad;
    private String horarioEntidad;
    private String webEntidad;

    /**
     * @return The id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id The id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return The title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title The title
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return The telefonoEntidad
     */
    public String getTelefonoEntidad() {
        return telefonoEntidad;
    }

    /**
     * @param telefonoEntidad The telefonoEntidad
     */
    public void setTelefonoEntidad(String telefonoEntidad) {
        this.telefonoEntidad = telefonoEntidad;
    }

    /**
     * @return The horarioEntidad
     */
    public String getHorarioEntidad() {
        return horarioEntidad;
    }

    /**
     * @param horarioEntidad The horarioEntidad
     */
    public void setHorarioEntidad(String horarioEntidad) {
        this.horarioEntidad = horarioEntidad;
    }

    /**
     * @return The webEntidad
     */
    public String getWebEntidad() {
        return webEntidad;
    }

    /**
     * @param webEntidad The webEntidad
     */
    public void setWebEntidad(String webEntidad) {
        this.webEntidad = webEntidad;
    }
}